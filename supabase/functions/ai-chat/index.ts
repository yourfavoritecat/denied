import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.39.3";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are the Denied Trip Assistant, an expert on medical tourism, especially in Mexico. You help patients plan medical tourism trips, answer questions about procedures, pricing, travel logistics, border crossings, pharmacies, recovery, and safety. You are friendly, direct, and reassuring. You speak from experience. When you don't know something specific, you say so and recommend the user contact their provider directly. You never give medical diagnoses or replace professional medical advice.

Keep responses concise and conversational â€” aim for 2-4 short paragraphs max unless the user asks for detail.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, userContext } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    // Build context-aware system prompt
    let contextAddition = "";
    if (userContext) {
      if (userContext.userName) {
        contextAddition += `\nThe user's name is ${userContext.userName}.`;
      }
      if (userContext.tripBriefs && userContext.tripBriefs.length > 0) {
        contextAddition += `\nThe user has these upcoming trip briefs: ${JSON.stringify(userContext.tripBriefs)}`;
      }
      if (userContext.bookings && userContext.bookings.length > 0) {
        contextAddition += `\nThe user has these bookings: ${JSON.stringify(userContext.bookings)}`;
      }
      if (userContext.currentPage) {
        contextAddition += `\nThe user is currently viewing: ${userContext.currentPage}`;
      }
    }

    const response = await fetch(
      "https://ai.gateway.lovable.dev/v1/chat/completions",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${LOVABLE_API_KEY}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          model: "google/gemini-3-flash-preview",
          messages: [
            { role: "system", content: SYSTEM_PROMPT + contextAddition },
            ...messages,
          ],
          stream: true,
        }),
      }
    );

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded, please try again shortly." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "AI credits exhausted. Please add credits in Settings." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(
        JSON.stringify({ error: "AI service unavailable" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("ai-chat error:", e);
    return new Response(
      JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
